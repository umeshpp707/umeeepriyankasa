package Conditionalstatements;

public class ladderif {

	public static void main(String[] args) {
	
		int a =20;
		int b =30;
		
		if(a>b) {
		
		System.out.println("1st if");	
	
		}
		
		if(a>b) {
			
		System.out.println("2nd if");	
		}
		 
		if(a>b) {
		
		System.out.println("3rd if");	
		
		}
		
		else {
		
		System.out.println("else block");	
		}
		
	}
	

}
